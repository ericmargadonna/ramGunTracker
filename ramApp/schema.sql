CREATE TABLE ITEM(
SERIALNUM TEXT PRIMARY KEY NOT NULL,
ITEMTYPE TEXT, 
MODELNUM TEXT
);

CREATE TABLE ITEMTRANSACTION(
ID INTEGER PRIMARY KEY NOT NULL,
SERIALNUM TEXT,
SHIPMENT INTEGER,
FOREIGN KEY (SERIALNUM) REFERENCES ITEM (SERIALNUM)
FOREIGN KEY (SHIPMENT) REFERENCES SHIPMENT (ID)
);

CREATE TABLE SHIPMENT(
ID INTEGER PRIMARY KEY NOT NULL,
GUNSER TEXT,
BASESER TEXT,
CABLE INTEGER,
DATE TEXT,
TRACKING TEXT,
NOTES TEXT,
COMPLETED INTEGER,
DIRECTION INTEGER,
INCIDENTNUM TEXT,
FOREIGN KEY (INCIDENTNUM) REFERENCES INCIDENT (INCIDENTNUM)
);

CREATE TABLE INCIDENT(
INCIDENTNUM TEXT PRIMARY KEY NOT NULL,
CALLDATE TEXT,
STORENUM TEXT,
STORECONTACT TEXT,
NOTES TEXT,
);

CREATE TABLE USER(
ID INTEGER PRIMARY KEY NOT NULL,
NAME TEXT UNIQUE,
HASH TEXT
);